#!/bin/sh
# ============================================================================
# Docker 容器启动脚本
# ============================================================================
# 这个脚本在容器启动时执行,负责:
# 1. 等待数据库就绪
# 2. 执行数据库迁移
# 3. 初始化种子数据(仅首次)
# 4. 启动应用
# ============================================================================

set -e

echo "=========================================="
echo "校园二手交易平台 - 启动中..."
echo "=========================================="

# 等待MySQL数据库就绪
echo "等待数据库连接..."
max_retries=30
counter=0

# 使用prisma db push同步数据库架构（适合开发环境，无需预先生成迁移文件）
# 如果数据库未就绪,push会失败
while ! npx prisma db push --skip-generate > /dev/null 2>&1; do
  counter=$((counter + 1))
  if [ $counter -gt $max_retries ]; then
    echo "数据库连接超时,请检查数据库配置"
    exit 1
  fi
  echo "等待数据库就绪... ($counter/$max_retries)"
  sleep 3
done

echo "数据库架构同步完成"

# 执行种子数据初始化
# prisma db seed 如果数据已存在会报错,但不影响启动
echo "初始化种子数据..."
npx prisma db seed 2>/dev/null || echo "种子数据已存在或初始化跳过"

echo "=========================================="
echo "初始化完成,启动应用服务..."
echo "API地址: http://localhost:3000/api"
echo "=========================================="

# 启动Node.js应用
exec node dist/src/main.js
